#include "libswap.h"

int		*ft_swap_stack(int **arr, int size)
{
	int tmp;

	tmp = arr[0][size];
	arr[0][size] = arr[0][size - 1];
	arr[0][size - 1] = tmp;
	return (arr[0]);
}
