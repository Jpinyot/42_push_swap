void	ft_push_stack(int **arr_a, int **arr_b, int *size_a, int *size_b)
{
	*size_b++;
	arr_b[0][*size_b] = arr_a[0][*size_a];
	arr_a[0][*size_a] = 0;
	*size_a--;
}
